package Entities;

import java.util.Objects;

/**
 *
 * @author Ahmed
 */
public class categorieCagnotte {
    protected int id;
    protected String nom;
    protected String description;

    public categorieCagnotte() {
    }
    
    public categorieCagnotte(String nom, String description) {
        this.nom = nom;
        this.description = description;
    }

    public categorieCagnotte(int id, String nom, String description) {
        this.id = id;
        this.nom = nom;
        this.description = description;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNom() {
        return nom;
    }

    public void setNom(String nom) {
        this.nom = nom;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    @Override
    public int hashCode() {
        int hash = 7;
        hash = 53 * hash + this.id;
        hash = 53 * hash + Objects.hashCode(this.nom);
        hash = 53 * hash + Objects.hashCode(this.description);
        return hash;
    }

    @Override
    public boolean equals(Object obj) {
        if (this == obj) {
            return true;
        }
        if (obj == null) {
            return false;
        }
        if (getClass() != obj.getClass()) {
            return false;
        }
        final categorieCagnotte other = (categorieCagnotte) obj;
        if (this.id != other.id) {
            return false;
        }
        if (!Objects.equals(this.nom, other.nom)) {
            return false;
        }
        if (!Objects.equals(this.description, other.description)) {
            return false;
        }
        return true;
    }

    @Override
    public String toString() {
        return "categorie{" + "id=" + id + ", nom=" + nom + ", description=" + description + '}';
    }
    
    
    
    
}
